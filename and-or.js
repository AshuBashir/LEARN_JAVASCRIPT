// AND OR OPERATOR
//and operator
let firstName = "Ashu";
let age = 25;
if(firstName[0]==='A' && age>18){
    console.log("Name starts with A and above 18");
}
else{
    console.log("not true");
}


//or operator
let firstName1 = "Ashu";
let age1 = 25;
if(firstName1[0]==='a' || age1>18){
    console.log("Name starts with A and above 18");
}
else{
    console.log("not true");
}